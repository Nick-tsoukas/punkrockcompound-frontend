<template>
  <div
    class="transition-all duration-1000 ml-0 flex gap-12 flex-nowrap overflow-y-scroll scroll-video"
  >
    <slot></slot>
  </div>
</template>

<script>
export default {
  mounted() {
    const slider = document.querySelector('.scroll-video')
    if (slider) {
      let isDown = false
      let startX
      let scrollLeft
      slider.addEventListener('mousedown', (e) => {
        isDown = true
        console.log('isdown: true')
        slider.classList.add('active')
        startX = e.pageX - slider.offsetLeft
        scrollLeft = slider.scrollLeft
      })
      slider.addEventListener('mouseleave', () => {
        isDown = false
        slider.classList.remove('active')
      })
      slider.addEventListener('mouseup', (e) => {
        isDown = false
        console.log('event: mouseup')
        slider.classList.remove('active')
      })
      slider.addEventListener('mousemove', (e) => {
        if (!isDown) return
        e.preventDefault()
        const x = e.pageX - slider.offsetLeft
        const walk = x - startX
        slider.scrollLeft = scrollLeft - walk
      })
    }
    console.log('slider in ', slider)
  },
}
</script>

<style land="css" scoped>
* {
  box-sizing: border-box;
}

.active {
  cursor: grabbing !important;
}

.scroll {
  cursor: grab;
}
</style>
